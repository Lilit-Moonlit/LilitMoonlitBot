def format_ad_post(data, ad_type):
    lines = []
    if ad_type == "model":
        lines.append("💃 <b>ПОШУК МОДЕЛІ</b>\n")
        fields = MODEL_FIELDS
    else:
        lines.append("💼 <b>ВАКАНСІЯ</b>\n")
        fields = VACANCY_FIELDS
        
    for field in fields:
        # Пропускаємо технічні поля медіа в тексті підпису
        if "photo" in field["key"] or "video" in field["key"]:
            continue
            
        value = data.get(field["key"])
        if value:
            safe_value = html.escape(str(value))
            lines.append(f"<b>{field['label']}</b> {safe_value}")
    
    return "\n".join(lines)
